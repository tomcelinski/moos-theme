	 	<h2><span>Q3.</span> Will the Mini Aero have wired connectivity in addition to wireless?</h2>
	 	<div class="inset-text"><p>No, the Mini Aero will not have wired connectivity in addition to wireless – here by wired connectivity we mean running audio over a wire to the speakers, and running a wire from one speaker to the other. We have thought hard about this. To properly support wired connectivity requires the inclusion of a considerable amount of additional electronics in what is already very limited space. To do this well we would not only sacrifice space inside the speaker, but would also add non-trivial amount of cost. We don’t believe this would add any significant benefits to the functionality or the operation of the Mini Aero, particularly in the context of the input hub which will accommodate a range of sources/inputs. </p>

