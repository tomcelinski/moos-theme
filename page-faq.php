<p>We have been doing wireless audio for a long time, both from the perspective of product design, as well as innovation in core wireless audio technologies. The Mini Aero is a culmination of this effort, a wireless speaker system that draws on a wealth of research and development experience. The diagram below gives you an idea of where we are at this point in time (mid April 2013). It also shows you the path we have taken. Designing a great product is not a straight path, at least not initially. A lot of effort goes into ideas, concepts, experiments, prototypes. We are now well past this phase, on a much straighter path towards production. This also takes, time – and sometimes includes surprises such as supplier delays – but it’s a well-understood process that reliably leads to product delivery. </p>

<p>The point here is to put time frames in perspective. It took us years to figure out the right way to do wireless audio. But it has only taken us around 12 months to go from the point where we decided to do the Mini Aero as our first wireless speakers to where we are today - a remarkably short time period given the complexity of what is involved (admittedly we started with a well designed passive version of the Mini Aero, which helped). We are nearly there, and are extremely excited about it! </p>

